package week1.assignments.optional;

public class Calculator {
	public int add (int a, int b, int c) {
		return a+b+c;
	}

	public int subtract(int a, int b) {
		return a-b;
	}

	public double multiply(double a, double b) {
		return (long) (a*b);
	}

	public float divide (int a, float b) {
		return a/b;
	}
}
